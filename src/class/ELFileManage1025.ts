'use strict';

// define modules
import * as path from 'node:path'; // path
import { promises, existsSync } from 'fs'; // file system
import { unlink, readdir } from 'node:fs/promises'; // file 
// file system definition
const { mkdir } = promises;

// FileManage class
class FileManage {
  static logger: any; // static logger

  // construnctor
  constructor(logger: any) {
    // logger setting
    FileManage.logger = logger;
    FileManage.logger.debug('filemanage: mkdir initialized.');
  }

  // mkDir
  mkDir = async (dir: string): Promise<void> => {
    return new Promise(async (resolve, _) => {
      try {
        FileManage.logger.debug('filemanage: mkdir started.');
        // not exists
        if (!existsSync(dir)) {
          // make dir
          await mkdir(dir);

          FileManage.logger.debug('filemanage: mkdir completed.');
        } else {
          FileManage.logger.debug('already exists.');
        }
        resolve();
      } catch (err: unknown) {
        // error
        FileManage.logger.error(err);
        resolve();
      }
    });
  };

  // mkDirAll
  mkDirAll = async (dirs: string[]): Promise<void> => {
    return new Promise(async (resolve1, _) => {
      try {
        FileManage.logger.debug('filemanage: all mkdir started.');
        // make all dir
        Promise.all(
          dirs.map(async (dir: string): Promise<void> => {
            return new Promise(async (resolve2, _) => {
              try {
                // not exists
                if (!existsSync(dir)) {
                  // make dir
                  await mkdir(dir);
                  resolve2();
                } else {
                  // error
                  throw Error('already exists.');
                }
              } catch (err: unknown) {
                // error
                resolve2();
              }
            });
          })
        ).then(() => resolve1());
        FileManage.logger.debug('filemanage: mkDirAll started.');

        // make dir
      } catch (e: unknown) {
        // error
        FileManage.logger(e);
        resolve1();
      }
    });
  };

  // rmDir
  rmDir = async (dir: string): Promise<void> => {
    return new Promise(async (resolve, _) => {
      try {
        FileManage.logger.debug('filemanage: rmDir started.');
        // txtfile list
        const tmpTxtFiles: string[] = await readdir(dir);
        // delete all files
        await Promise.all(tmpTxtFiles.map((fl: string): Promise<void> => {
          return new Promise(async (resolve, _) => {
            try {
              // txt file path
              const targetPath: string = path.join(dir, fl);
              await unlink(targetPath);
              // result
              resolve();

            } catch (err2: unknown) {
              FileManage.logger.error(err2);
            }
          })
        }));
        // result
        resolve();
      } catch (err: unknown) {
        // error
        FileManage.logger.error(err);
        resolve();
      }
    });
  };
}

// export module
export default FileManage;
